/// Enum representing the two modes of operation for a stethoscope.
enum StethoscopeMode {
  /// The stethoscope is in 'recording' mode. In this mode, it captures and
  /// stores the sounds it hears.
  recording,

  /// The stethoscope is in 'listening' mode. In this mode, it only listens to
  /// current sounds without recording them.
  listening,
}
